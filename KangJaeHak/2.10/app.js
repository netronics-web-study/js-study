const calculator = {
  add: function (a, b) {
    console.log(a + b);
  },
  sub: function (a, b) {
    console.log(a - b);
  },
  mul: function (a, b) {
    console.log(a * b);
  },
  div: function (a, b) {
    console.log(a / b);
  },
  mod: function (a, b) {
    console.log(a % b);
  },
  pow: function (a, b) {
    console.log(a ^ b);
  },
};

calculator.add(5, 2);
calculator.sub(5, 2);
calculator.mul(5, 2);
calculator.div(5, 2);
calculator.mod(5, 2);
calculator.pow(5, 2);
